let readyGateCompleted = false;

nextSongPreload.preload = "auto";

function formatTime(seconds) {
  if (!Number.isFinite(seconds)) return "00:00";

  const minutes = Math.floor(seconds / 60);
  const remainingSeconds = Math.floor(seconds % 60);
  return `${String(minutes).padStart(2, "0")}:${String(remainingSeconds).padStart(2, "0")}`;
}

function normalizeMessage(text) {
  return text
    .toLowerCase()
    .replace(/[?!.,]/g, "")
    .replace(/\s+/g, " ")
    .trim();
}

function isReadyGateText(text) {
  return normalizeMessage(text) === "siap";
}

function getReadingSeconds() {
  if (!textStartTime) return song.currentTime || 0;

  const pauseOffset = pauseStartedAt ? performance.now() - pauseStartedAt : 0;
  return Math.max(0, (performance.now() - textStartTime - totalPausedMs - pauseOffset) / 1000);
}

function pauseReading() {
  if (!pauseStartedAt) {
    pauseStartedAt = performance.now();
  }
}

function resumeReading() {
  if (!pauseStartedAt) return;

  totalPausedMs += performance.now() - pauseStartedAt;
  pauseStartedAt = null;
}

function estimateParagraphDuration(text) {
  const wordCount = text.split(/\s+/).filter(Boolean).length;
  const punctuationCount = (text.match(/[,.?!]/g) || []).length;
  const readingSeconds = (wordCount / READING_WORDS_PER_MINUTE) * 60;
  const pauseSeconds = PARAGRAPH_PAUSE_SECONDS + punctuationCount * 0.12;

  return Math.max(MIN_PARAGRAPH_SECONDS, readingSeconds + pauseSeconds);
}

function buildCues(duration) {
  const paragraphDurations = paragraphs.map(estimateParagraphDuration);
  const estimatedReadingDuration = paragraphDurations.reduce((sum, seconds) => sum + seconds, 0);
  const readableDuration = Math.max(estimatedReadingDuration, (duration || 0) * 1.06, 1);
  const scale = readableDuration / estimatedReadingDuration;
  let cursor = 0;

  cues = paragraphs.map((text, index) => {
    const slice = index === paragraphs.length - 1
      ? readableDuration - cursor
      : paragraphDurations[index] * scale;
    const cue = {
      text,
      start: cursor,
      end: Math.min(readableDuration, cursor + slice),
    };

    cursor = cue.end;
    return cue;
  });

  if (cues.length) {
    cues[cues.length - 1].end = readableDuration;
  }
}

function setMessage(index) {
  if (index === currentIndex || !cues[index]) return;

  currentIndex = index;
  const shouldShowPhoto = cues[index].text.includes("ke foto kamu");
  const shouldShowReadyPrompt = isReadyGateText(cues[index].text) && !readyGateCompleted;
  const length = cues[index].text.length;
  const size = length > 300
    ? "clamp(1.15rem, 2.7vw, 1.7rem)"
    : length > 210
      ? "clamp(1.24rem, 3vw, 1.95rem)"
      : length > 130
        ? "clamp(1.45rem, 3.6vw, 2.35rem)"
        : "clamp(1.8rem, 4.7vw, 3.1rem)";

  messageText.classList.remove("is-changing");
  textFrame.classList.remove("is-turning");
  void textFrame.offsetWidth;
  messageText.style.setProperty("--message-size", size);
  messageText.textContent = cues[index].text;
  messageText.classList.add("is-changing");
  textFrame.classList.add("is-turning");

  memoryPhoto.classList.toggle("is-visible", shouldShowPhoto);
  if (shouldShowPhoto) {
    memoryPhoto.getAnimations().forEach((animation) => {
      animation.cancel();
      animation.play();
    });
  }

  readyPrompt.classList.toggle("is-visible", shouldShowReadyPrompt);
  readyPrompt.setAttribute("aria-hidden", String(!shouldShowReadyPrompt));
  if (shouldShowReadyPrompt) {
    pauseReading();
    readyHint.hidden = false;
    readyButton.hidden = false;
    readyChoice.hidden = true;
    readyAnswer.textContent = "";
  }
}

function syncFrame() {
  const textDuration = cues.at(-1)?.end || 0;
  const current = getReadingSeconds();
  const activeIndex = cues.findIndex((cue, index) => {
    const nextCue = cues[index + 1];
    return current >= cue.start && (!nextCue || current < nextCue.start);
  });

  setMessage(activeIndex < 0 ? 0 : activeIndex);

  if (textDuration) {
    const progress = Math.min(100, (current / textDuration) * 100);
    progressFill.style.width = `${progress}%`;
    timeText.textContent = `${formatTime(current)} / ${formatTime(textDuration)}`;
  }

  if (cues.at(-1) && current >= cues.at(-1).end) {
    setMessage(cues.length - 1);
    scheduleEndingQuestion();
    rafId = null;
    return;
  }

  rafId = requestAnimationFrame(syncFrame);
}

function scheduleEndingQuestion() {
  if (endingScheduled) return;

  endingScheduled = true;
  window.clearTimeout(endingTimer);
  endingTimer = window.setTimeout(showEndingQuestion, 30_000);
}

function showEndingQuestion() {
  endingQuestion.classList.add("is-visible");
  endingQuestion.setAttribute("aria-hidden", "false");
}

function showConfirm() {
  confirmLayer.classList.add("is-visible");
  confirmLayer.setAttribute("aria-hidden", "false");
}

function hideConfirm() {
  confirmLayer.classList.remove("is-visible");
  confirmLayer.setAttribute("aria-hidden", "true");
}

// Playlist functions
function loadSong(index) {
  const track = PLAYLIST[Math.min(index, PLAYLIST.length - 1)];
  song.src = track.src;
  song.loop = index >= PLAYLIST.length - 1;
  songTitle.textContent = `Lagu: ${track.title}`;
  currentSongIndex = Math.min(index, PLAYLIST.length - 1);
  song.load();
  preloadNextSong();
}

function preloadNextSong() {
  if (currentSongIndex >= PLAYLIST.length - 1) return;

  nextSongPreload.src = PLAYLIST[currentSongIndex + 1].src;
  nextSongPreload.load();
}

function playNextSong() {
  if (currentSongIndex >= PLAYLIST.length - 1) {
    song.loop = true;
    song.play().catch(() => {});
    return;
  }

  const nextIndex = currentSongIndex + 1;
  loadSong(nextIndex);
  song.currentTime = 0;
  song.play().catch(() => {
    // Handle autoplay restrictions
    experience.addEventListener("click", () => song.play(), { once: true });
  });
}

async function startExperience() {
  hideConfirm();
  intro.classList.remove("is-visible");
  experience.classList.add("is-visible");
  experience.setAttribute("aria-hidden", "false");
  endingQuestion.classList.remove("is-visible");
  endingQuestion.setAttribute("aria-hidden", "true");
  endingActions.hidden = false;
  endingAnswer.textContent = "";
  endingScheduled = false;
  readyGateCompleted = false;
  totalPausedMs = 0;
  pauseStartedAt = null;
  readyPrompt.classList.remove("is-visible");
  readyPrompt.setAttribute("aria-hidden", "true");
  readyHint.hidden = false;
  readyButton.hidden = false;
  readyChoice.hidden = true;
  readyAnswer.textContent = "";
  window.clearTimeout(endingTimer);

  song.loop = false;
  song.muted = false;
  song.volume = 1;

  // Load first song
  loadSong(0);

  buildCues(0);
  setMessage(0);
  textStartTime = performance.now();
  cancelAnimationFrame(rafId);
  syncFrame();

  try {
    song.currentTime = 0;
  } catch {
    // Some browsers only allow seeking after metadata is ready.
  }

  const playAttempt = song.play();
  try {
    await playAttempt;
  } catch {
    const retryPlay = async () => {
      try {
        song.muted = false;
        song.volume = 1;
        await song.play();
      } catch {
        messageText.textContent = "Browser masih menahan lagunya. Klik sekali lagi ya.";
      }
    };

    messageText.textContent = "Klik layar sekali lagi untuk memulai lagunya.";
    experience.addEventListener("click", retryPlay, { once: true });
  }
}

